    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // TODO add your handling code here:
            Usuario usuario = usuarioController.buscarUsuarioPorLogin(login.getText());
            String pass = String.valueOf(senha.getPassword());
            if(usuario.getLogin().equals(login.getText()) && usuario.getSenha().equals(pass)){
            if(usuario.getTipo().equals("Gerente")){
                new MenuGerente().setVisible(true);
            this.dispose();
            }
             else if(usuario.getTipo().equals("Gar√ßom")){
                    new MenuGarcom().setVisible(true);
            this.dispose();
                }
            }else {
                 JOptionPane.showMessageDialog(this, "Erro ao entrar no sistema: Login ou senha incorretos. ");
            }
            
            // if (login.getText().equals())
        } catch (NegocioException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Erro ao entrar no sistema: Login ou senha incorretos. ");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

