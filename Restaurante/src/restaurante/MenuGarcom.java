  private void preencherJtable() throws NegocioException {
        //Aqui carrego minha lista
        ArrayList<Produto> itens = controle.array();
         DefaultTableModel tableProdutos = (DefaultTableModel) tabelaProdutos.getModel();

        
              //Aqui verifico se a jTable tem algum registo se tiver eu deleto
        while (tableProdutos.getRowCount() > 0) {
            tableProdutos.removeRow(0);
        }

                 //Aqui eu adiciono cada linha da lista na jTable
        for (Produto p : itens) {
                      
                     
            tableProdutos.addRow(new Object[] { 
                    p.getNome(),
                    p.getPorcao(),
                    p.getValor(),
                    p.getCategoria(),
                    p.getQuantEstoque()
            
            });
        }
    }
