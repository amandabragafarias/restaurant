    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        if(tabelaProdutos.getSelectedRow() != -1){
            String nome = tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 0).toString();
            String categoria = tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 3).toString();
            String porcao = tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 1).toString();
            String quantEstoque = tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 4).toString();
            String valor = tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 2).toString();
            
            
            Produto produto = new Produto(null, null, 0.0, null, 0);
            produto.setCategoria(categoria);
            produto.setPorcao(porcao);
            produto.setNome(nome);
            produto.setValor(Double.parseDouble(valor));
            produto.setQuantEstoque(Integer.parseInt(quantEstoque));
            Conta conta = new Conta (null, null, null, 0, null);
           
            try { 
                conta = controle.buscarConta(c);
                controle.associarContaProduto(conta, produto);
            } catch (NegocioException ex) {
                Logger.getLogger(MenuGarcom.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(this, " Pedido realizado com sucesso!");
         }else{
            JOptionPane.showMessageDialog(this, "");
        }
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

